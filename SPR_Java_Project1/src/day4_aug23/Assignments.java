package day4_aug23;

public class Assignments {

	public static void main(String[] args) {
		
		// To print a triangle using *
		
		int r=5;
		
		for(int i=1; i<=r; i++)
		{
			for(int st=1; st<=i; st++)
			{
				System.out.print(" * ");
			}
			
			System.out.println();
		}
		
		
	}

}
