package day4_aug23;

public class LoopStatements {

	public static void main(String[] args) {
		// To print numbers from 1 to 10
		// While Loop
		
		int i = 1;
		
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}
			
		// do-while loop
		
		int j=1;
		
		do
		{
			System.out.println(j);
			j++;
		}
		while(j<=10);
		
		// for loop
		
		for(int k=1;k<=15;k++)
		{
			System.out.println(k);
		}
	}

}
